function [y] = SuperCalculator(a,b)

%SUPERCALCULATOR Adds two numbers together
%
%   [y] = SuperCalculator(a,b) adds a and b together and returns the sum as
%   the value y.
%
%   Example usage
%
%       %Add two integers
%       a = 1;
%       b = 2;
%       [y] = SuperCalculator(a,b);
%
%INPUT:     -a: first number
%           -b: second number
%
%OUTPUT:    -y: a+b
%
%Christopher Lum
%lum@uw.edu

%Version History
%06/21/24: Created

%-------------------------BEGIN CALCULATIONS-------------------------------
%Add numbers together
y = a+b;